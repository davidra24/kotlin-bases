package operadores

fun main(args: Array<String>){
    println(1+1)
    println(3-1)
    println(2*2)
    println(4/2)
    println(7%2)

    val a = 4
    val b = 2

    println("suma de objetos: "+a.plus(b))
    println("resta de objetos: "+a.minus(b))
    println("multiplicación de objetos: "+a.times(b))
    println("división de objetos: "+a.div(b))

}
